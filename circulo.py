class Circulo:
    radio = 0
    area = radio *3.1415
    perimetro = 2 * radio * 3.1415

    def __init__(self, radio=radio):
        self.radio = radio

    def calcular_area(self):
         return 3.1415* self.radio ** 2

    def calcular_perimetro(self):
        return 3.1415 * self.radio * 2




